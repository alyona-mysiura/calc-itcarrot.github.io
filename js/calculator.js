function sizeWindow() {
  input_h = $("#output-h").val();
  input_w = $("#output-w").val();
  input_w_balkon = $("#output-w2").val();
  input_h_balkon = Number(input_h) - 800;

  for(var i = 1; i < sizeLH; i++) {
    if (input_h == size[type_window][0][i-1]) {
      height = i-1;
      i = sizeLH;
    }
    else if (input_h > size[type_window][0][i-1] & input_h <= size[type_window][0][i]) {
      height = i;
      i = sizeLH;
    }
  }

  for(var j = 1; j < sizeLW; j++) {
    if (input_w == size[type_window][1][j-1]) {
      width = j-1;
      j = sizeLW;
    }
    else if (input_w > size[type_window][1][j-1] & input_w <= size[type_window][1][j]) {
      width = j;
      j = sizeLW;
    }
  }

  for(var q = 1; q < sizeLHOdn; q++) {
    if (input_h_balkon == size[0][0][q-1]) {
      height_balkon = q-1;
      q = sizeLHOdn;
    }
    else if (input_h_balkon > size[0][0][q-1] & input_h_balkon <= size[0][0][q]) {
      height_balkon = q;
      q = sizeLHOdn;
    }
  }

  for(var k = 1; k < sizeLWOdn; k++) {
    if (input_w_balkon == size[0][1][k-1]) {
      width_balkon = k-1;
      k = sizeLWOdn;
    }
    else if (input_w_balkon > size[0][1][k-1] & input_w_balkon <= size[0][1][k]) {
      width_balkon = k;
      k = sizeLWOdn;
    }
  }
}

function costAdt() {
  cost_sill = 0;
  cost_otl = 0;

  for(var i = 1; i < price_sill[1].length; i++) {
    if (value_sill == price_sill[0][i-1]) {
      sill = i-1;
      cost_sill = (Number(input_w) + 20) / 1000 * Number(price_sill[1][sill]);
      i = price_sill[1].length;
    }
    else if (value_sill > price_sill[0][i-1] & value_sill <= price_sill[0][i]) {
      sill = i;
      cost_sill = (Number(input_w) + 20) / 1000 * Number(price_sill[1][sill]);
      i = price_sill[1].length;
    }
  }

  for(var j = 1; j < price_otl[1].length; j++) {
    if (value_otl == price_otl[0][j-1]) {
      otl = j-1;
      cost_otl = Number(input_w) / 1000 * Number(price_otl[1][otl]);
      j = price_otl[1].length;
    }
    else if (value_otl > price_otl[0][j-1] & value_otl <= price_otl[0][j]) {
      otl = j;
      cost_otl = Number(input_w) / 1000 * Number(price_otl[1][otl]);
      j = price_otl[1].length;
    }
  }
}

function outPrice() {
  type_standart = $(".windiw_variants li.active").data("type");
  price_standart = standart[type_window][type_standart];
  price_standart_balkon = standart[0][type_standart];
  nats = Number(natsenka[type_standart])/100;
  skid = Number(skidka[type_standart])/100;

  sizeWindow();
  costAdt();

  if (type_window != 6) {
    p = Number(t_window[type_window][type_standart][height][width]) * Number(price_standart) + cost_otl + cost_sill + ((input_h / 1000) * (input_w / 1000)) * mosc + ((input_h / 1000) * (input_w / 1000)) * mont;
    p = p + p*nats - p*skid;
    rostroch = p/6;
  }
  else {
    p = (Number(t_window[type_window][type_standart][height][width]) * Number(price_standart) + ((input_h / 1000) * (input_w / 1000)) * mont) + (Number(t_window[0][type_standart][height_balkon][width_balkon]) * Number(price_standart_balkon) + cost_otl + cost_sill + (((input_h - 800) /1000) * (input_w_balkon / 1000)) * mont);
    p = p + p*nats - p*skid;
    rostroch = p/6;
  }
  
  $("#"+totalIdActive+" .item_price .c-price").text(p.toFixed(0));
  $("#"+totalIdActive+" .c-credit-price").text(rostroch.toFixed(0));

  all_p = 0;

  $('.calc_top .c-price').each(function (index, data) {
    all_p = all_p + Number($(data).text());
  });

  $('.all_price').text(all_p);
}

